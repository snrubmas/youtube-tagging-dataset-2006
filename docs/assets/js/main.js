// Initialize all functions when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    animateNumbers();
    lazyLoadImages();
    addScrollAnimations();
    trackOutboundLinks();
    
    // Add loading state removal
    document.body.classList.add('loaded');
});

